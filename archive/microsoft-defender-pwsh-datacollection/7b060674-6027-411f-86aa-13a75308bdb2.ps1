        $UserCOMList += Get-ComObjectsFromOfflineHive -Hive $Hive.Root
        if($Hive.Root.SubkeyExist("WOW6432Node"))
        {
            $UserCOMList += Get-ComObjectsFromOfflineHive -Hive $Hive.Root.OpenSubKey("WOW6432Node")
